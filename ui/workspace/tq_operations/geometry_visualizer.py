    def handle_line_click(self, line, event):
        """Handle mouse clicks on lines.
        
        Manages line selection behavior:
        - Ctrl+click for multiple selection
        - Single click for single selection
        - Updates line appearance based on selection state
        
        Args:
            line (GeometryLine): The clicked line
            event (QMouseEvent): The mouse event
        """
        if not self.drawing_mode and line in self.parent_widget.selectable_lines:
            if event.modifiers() == Qt.ControlModifier:
                if line in self.selected_lines:
                    self.selected_lines.remove(line)
                    self.restore_line_appearance(line)
                else:
                    self.selected_lines.add(line)
                    line.setPen(QPen(self.line_selected_color, 4))
            else:
                for selected_line in self.selected_lines:
                    self.restore_line_appearance(selected_line)
                self.selected_lines.clear()
                self.selected_lines.add(line)
                line.setPen(QPen(self.line_selected_color, 4))

    def restore_line_appearance(self, line):
        """Restore original line appearance based on line type"""
        if isinstance(line, GeometryLine):
            if line.line_type == GeometryLine.Type.PATTERN:
                line.setPen(QPen(QColor(255, 165, 0), 2))  # Orange, normal width
            elif line.line_type == GeometryLine.Type.CUSTOM:
                line.setPen(QPen(QColor(255, 165, 0), 4))  # Orange, thicker
            # Base lines shouldn't need restoration as they can't be selected

