class GeometryConnectionManager:
    """Manages geometric connections between points.
    
    Tracks:
    - Point-to-point connections
    - Connection metadata (type, creation time, properties)
    - Connection history
    - Connection patterns
    """
    def __init__(self):
        self.connections = {}  # Graph structure: point_num -> {connected_point: line_ref}
        self.connection_metadata = {}  # (point1, point2) -> metadata
        self.connection_history = []  # List of (action, point1, point2, metadata)
        
    def add_connection(self, start_num, end_num, line_type, line_ref):
        """Add a new connection with metadata"""
        # Ensure points are ordered consistently
        point1, point2 = sorted([start_num, end_num])
        
        # Initialize connection points if needed
        if point1 not in self.connections:
            self.connections[point1] = {}
        if point2 not in self.connections:
            self.connections[point2] = {}
            
        # Add bidirectional connection
        self.connections[point1][point2] = line_ref
        self.connections[point2][point1] = line_ref
        
        # Store metadata
        metadata = {
            'type': line_type,
            'created_at': time.time(),
            'line_ref': line_ref
        }
        self.connection_metadata[(point1, point2)] = metadata
        
        # Record in history
        self.connection_history.append(('add', point1, point2, metadata))
        
    def remove_connection(self, start_num, end_num):
        """Remove a connection and update history"""
        point1, point2 = sorted([start_num, end_num])
        
        if point1 in self.connections and point2 in self.connections[point1]:
            # Record in history before removing
            metadata = self.connection_metadata.get((point1, point2), {})
            self.connection_history.append(('remove', point1, point2, metadata))
            
            # Remove bidirectional connection
            del self.connections[point1][point2]
            del self.connections[point2][point1]
            
            # Clean up empty connection points
            if not self.connections[point1]:
                del self.connections[point1]
            if not self.connections[point2]:
                del self.connections[point2]
                
            # Remove metadata
            if (point1, point2) in self.connection_metadata:
                del self.connection_metadata[(point1, point2)]

class PatternCache:
    """Cache for frequently accessed geometric patterns"""
    def __init__(self, max_size=100):
        self.cache = {}
        self.max_size = max_size
        self.access_count = {}
        
    def get(self, key):
        if key in self.cache:
            self.access_count[key] += 1
            return self.cache[key]
        return None
        
    def put(self, key, value):
        if len(self.cache) >= self.max_size:
            # Remove least accessed item
            min_key = min(self.access_count.items(), key=lambda x: x[1])[0]
            del self.cache[min_key]
            del self.access_count[min_key]
            
        self.cache[key] = value
        self.access_count[key] = 1

class GeometryPoint(QGraphicsEllipseItem):
    """Interactive point item with number label."""
    def __init__(self, x, y, number, radius=5):
        super().__init__(-radius, -radius, radius*2, radius*2)
        self.number = number
        self.setPos(x, y)
        self.setup_appearance()
        self.setup_label()
        
    def setup_appearance(self):
        self.setFlag(QGraphicsItem.ItemIsSelectable)
        self.setBrush(QBrush(QColor("dodgerblue")))
        self.setPen(QPen(QColor("dodgerblue")))
        self.setZValue(10)  # Keep points above lines
        
    def setup_label(self):
        if self.scene():
            self.label = self.scene().addText(str(self.number))
            self.label.setDefaultTextColor(QColor("black"))
            self.update_label_position()
            
    def update_label_position(self):
        if hasattr(self, 'label') and self.label:
            pos = self.scenePos()
            self.label.setPos(pos.x() - 10, pos.y() - 25)

class GeometryLine(QGraphicsLineItem):
    class Type:
        BASE = 1    # Structural lines (polygon edges)
        PATTERN = 2 # Pattern lines (radial connections)
        CUSTOM = 3  # User-drawn lines

    def __init__(self, start_point, end_point, line_type):
        p1 = start_point.scenePos()
        p2 = end_point.scenePos()
        super().__init__(p1.x(), p1.y(), p2.x(), p2.y())
        self.start_point = start_point
        self.end_point = end_point
        self.line_type = line_type
        self.setup_appearance()
        
    def setup_appearance(self):
        self.setFlag(QGraphicsItem.ItemIsSelectable)
        if self.line_type == self.Type.BASE:
            self.setPen(QPen(QColor(70, 130, 180), 2))
        elif self.line_type == self.Type.PATTERN:
            self.setPen(QPen(QColor(255, 165, 0), 2))
        else:  # CUSTOM
            self.setPen(QPen(QColor(255, 165, 0), 4))
        self.setZValue(5)  # Above background, below points

class GeometryScene(QGraphicsScene):
    def __init__(self):
        super().__init__()
        self.last_clicked_point = None
        self.drawing_mode = True
        self.preview_line = None
        
    def create_centered_polygon(self, sides, layers, layer_distance=50):
        """Create a centered polygon pattern with given number of sides and layers"""
        # Create center point
        center = GeometryPoint(0, 0, 1)
        self.addItem(center)
        points = [center]
        
        point_number = 2
        
        # Create each layer
        for layer in range(1, layers + 1):
            layer_points = []
            
            # Create points for this layer
            for i in range(sides):
                angle = 2 * math.pi * i / sides
                x = layer * layer_distance * math.cos(angle)
                y = layer * layer_distance * math.sin(angle)
                
                point = GeometryPoint(x, y, point_number)
                self.addItem(point)
                layer_points.append(point)
                points.append(point)
                point_number += 1
            
            # Create polygon edges
            for i in range(sides):
                line = GeometryLine(
                    layer_points[i],
                    layer_points[(i + 1) % sides],
                    GeometryLine.Type.BASE
                )
                self.addItem(line)
            
            # Create radial connections to center
            for point in layer_points:
                line = GeometryLine(
                    center,
                    point,
                    GeometryLine.Type.PATTERN
                )
                self.addItem(line)
                
        return points

    def get_nearest_point(self, pos, max_distance=20):
        """Find nearest point to given position"""
        items = self.items(QRectF(
            pos.x() - max_distance,
            pos.y() - max_distance,
            max_distance * 2,
            max_distance * 2
        ))
        
        nearest = None
        min_dist = float('inf')
        
        for item in items:
            if isinstance(item, GeometryPoint):
                dist = (item.scenePos() - pos).manhattanLength()
                if dist < min_dist and dist <= max_distance:
                    min_dist = dist
                    nearest = item
                    
        return nearest

    def get_nearest_line(self, pos, max_distance=20):
        """Find nearest line to given position"""
        items = self.items(QRectF(
            pos.x() - max_distance,
            pos.y() - max_distance,
            max_distance * 2,
            max_distance * 2
        ))
        
        nearest = None
        min_dist = float('inf')
        
        for item in items:
            if isinstance(item, GeometryLine):
                dist = self.distance_to_line_segment(
                    pos,
                    item.line().p1(),
                    item.line().p2()
                )
                if dist < min_dist and dist <= max_distance:
                    min_dist = dist
                    nearest = item
                    
        return nearest