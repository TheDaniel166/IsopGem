class GeometryLine(QGraphicsLineItem):
    """A custom line item that can be selected and styled based on its type.
    
    Types:
        BASE: Structural lines that form the basic geometry (non-selectable)
        PATTERN: Lines generated by pattern algorithms
        CUSTOM: User-drawn lines
    
    Attributes:
        line_type: The type of line (BASE, PATTERN, or CUSTOM)
    """
    
    class Type:
        """Enumeration of line types"""
        BASE = 1    # Structural lines (polygon/star base)
        PATTERN = 2 # Generated from patterns
        CUSTOM = 3  # User-drawn

    def __init__(self, x1, y1, x2, y2, line_type, parent=None):
        super().__init__(x1, y1, x2, y2, parent)
        self.line_type = line_type
        self.start_num = None  # Store point numbers
        self.end_num = None
        self.setAcceptHoverEvents(True)
        self.setFlag(QGraphicsItem.ItemIsSelectable, False)
        self.setFlag(QGraphicsItem.ItemIsFocusable, True)
        
        # Set lower Z value for lines
        self.setZValue(1)  # Lines stay above background but below points
        
        # Set appearance based on type
        if line_type == self.Type.BASE:
            self.setPen(QPen(QColor(70, 130, 180), 2))
        elif line_type == self.Type.PATTERN:
            self.setPen(QPen(QColor(255, 165, 0), 2))
        else:  # CUSTOM
            self.setPen(QPen(QColor(255, 165, 0), 4))

    @classmethod
    def from_points(cls, start_pos, end_pos, line_type):
        """Create a line from two QPointF objects.
        
        Args:
            start_pos (QPointF): Starting point of the line
            end_pos (QPointF): Ending point of the line
            line_type (Type): Type of line to create
            
        Returns:
            GeometryLine: New line instance
        """
        return cls(
            start_pos.x(), start_pos.y(),
            end_pos.x(), end_pos.y(),
            line_type
        )

    def mousePressEvent(self, event):
        """Handle mouse press on the line"""
        if event.button() == Qt.LeftButton:
            # Let the view handle the selection
            view = self.scene().views()[0]
            view.handle_line_click(self, event)
            event.accept()
        else:
            super().mousePressEvent(event)

    def set_endpoints(self, start_num, end_num):
        """Store the point numbers this line connects"""
        self.start_num = start_num
        self.end_num = end_num

@dataclass
class Point:
    """Represents a point in the geometry system"""
    item: QGraphicsEllipseItem
    position: QPointF
    number: int
    connections: Set[int] = field(default_factory=set)  # Connected point numbers

@dataclass
class Line:
    """Represents a line in the geometry system"""
    item: GeometryLine
    start: int  # Point number
    end: int    # Point number
    type: int   # Line type

class SpatialGrid:
    """Optimized spatial partitioning system"""
    def __init__(self, cell_size: int = 50, bounds: QRectF = None):
        self.cell_size = cell_size
        self.points: Dict[Tuple[int, int], List[Point]] = {}
        self.lines: Dict[Tuple[int, int], List[Line]] = {}
        self.point_lookup: Dict[int, Point] = {}  # number -> Point
        self.line_lookup: Dict[Tuple[int, int], Line] = {}  # (start, end) -> Line
        
        # Add QuadTree for points and lines
        self.point_tree = QuadTree(bounds or QRectF(-1000, -1000, 2000, 2000))
        self.line_tree = QuadTree(bounds or QRectF(-1000, -1000, 2000, 2000))
        
    def get_cell(self, pos: QPointF) -> Tuple[int, int]:
        """Get cell coordinates for a position"""
        return (int(pos.x() // self.cell_size), 
                int(pos.y() // self.cell_size))

    def add_point(self, point: Point) -> None:
        """Add a point to the grid"""
        cell = self.get_cell(point.position)
        if cell not in self.points:
            self.points[cell] = []
        self.points[cell].append(point)
        self.point_lookup[point.number] = point
        
        # Add to QuadTree
        bounds = QRectF(point.position.x() - 5, point.position.y() - 5, 10, 10)
        self.point_tree.insert(point, bounds)

    def add_line(self, line: Line, bounds: QRectF) -> None:
        """Add a line to all cells it intersects"""
        # Get all cells the line's bounding box intersects
        min_cell = self.get_cell(bounds.topLeft())
        max_cell = self.get_cell(bounds.bottomRight())
        
        # Debug output
        print(f"Adding line to cells from {min_cell} to {max_cell}")
        
        for x in range(min_cell[0], max_cell[0] + 1):
            for y in range(min_cell[1], max_cell[1] + 1):
                cell = (x, y)
                if cell not in self.lines:
                    self.lines[cell] = []
                self.lines[cell].append(line)
                print(f"Added line to cell {cell}")

    def remove_line(self, line: Line) -> None:
        """Remove a line from the grid"""
        # Remove from cells
        for cell_lines in self.lines.values():
            if line in cell_lines:
                cell_lines.remove(line)
        
        # Remove from lookups
        self.line_lookup.pop((line.start, line.end), None)
        self.line_lookup.pop((line.end, line.start), None)
        
        # Update point connections
        self.point_lookup[line.start].connections.remove(line.end)
        self.point_lookup[line.end].connections.remove(line.start)

    def get_nearby_items(self, pos: QPointF, radius: float) -> Tuple[List[Point], List[Line]]:
        """Get all items within radius of position"""
        center_cell = self.get_cell(pos)
        cell_radius = int(radius // self.cell_size) + 1
        
        nearby_points: List[Point] = []
        nearby_lines: List[Line] = []
        checked_cells: Set[Tuple[int, int]] = set()
        
        # Check cells in radius
        for dx in range(-cell_radius, cell_radius + 1):
            for dy in range(-cell_radius, cell_radius + 1):
                cell = (center_cell[0] + dx, center_cell[1] + dy)
                if cell in checked_cells:
                    continue
                    
                checked_cells.add(cell)
                if cell in self.points:
                    nearby_points.extend(self.points[cell])
                if cell in self.lines:
                    nearby_lines.extend(self.lines[cell])
        
        return nearby_points, nearby_lines

    def get_line_cells(self, start: QPointF, end: QPointF) -> Set[Tuple[int, int]]:
        """Get all grid cells that a line passes through using DDA algorithm"""
        cells = set()
        start_cell = self.get_cell(start)
        end_cell = self.get_cell(end)
        
        dx = end_cell[0] - start_cell[0]
        dy = end_cell[1] - start_cell[1]
        steps = max(abs(dx), abs(dy))
        
        if steps == 0:
            return {start_cell}
            
        x_inc = dx / steps
        y_inc = dy / steps
        
        x, y = start_cell
        for _ in range(steps + 1):
            cells.add((int(x), int(y)))
            x += x_inc
            y += y_inc
        
        return cells

    def is_connected(self, point1: int, point2: int) -> bool:
        """Check if two points are connected"""
        return point2 in self.point_lookup[point1].connections

    def get_point(self, number: int) -> Optional[Point]:
        """Get point by number"""
        return self.point_lookup.get(number)

    def get_line(self, start: int, end: int) -> Optional[Line]:
        """Get line by endpoint numbers"""
        return self.line_lookup.get((start, end))

class QuadTree:
    """Spatial indexing for faster item lookup"""
    def __init__(self, bounds, max_items=10, max_depth=6):
        self.bounds = bounds  # QRectF
        self.max_items = max_items
        self.max_depth = max_depth
        self.items = []
        self.children = []
        self.depth = 0
        
    def subdivide(self):
        """Split node into four children"""
        x = self.bounds.x()
        y = self.bounds.y()
        w = self.bounds.width() / 2
        h = self.bounds.height() / 2
        
        self.children = [
            QuadTree(QRectF(x, y, w, h), self.max_items, self.max_depth),
            QuadTree(QRectF(x + w, y, w, h), self.max_items, self.max_depth),
            QuadTree(QRectF(x, y + h, w, h), self.max_items, self.max_depth),
            QuadTree(QRectF(x + w, y + h, w, h), self.max_items, self.max_depth)
        ]
        for child in self.children:
            child.depth = self.depth + 1

    def insert(self, item, bounds):
        """Insert item with its bounds"""
        if not self.bounds.intersects(bounds):
            return False
            
        if len(self.children) == 0:
            if len(self.items) < self.max_items or self.depth >= self.max_depth:
                self.items.append((item, bounds))
                return True
            else:
                self.subdivide()
                
        # Try to insert into children
        for child in self.children:
            if child.insert(item, bounds):
                return True
                
        # If couldn't insert into children, keep in current node
        self.items.append((item, bounds))
        return True

    def query(self, search_bounds):
        """Find all items intersecting search bounds"""
        found_items = []
        
        if not self.bounds.intersects(search_bounds):
            return found_items
            
        # Check items at this level
        for item, bounds in self.items:
            if bounds.intersects(search_bounds):
                found_items.append(item)
                
        # Check children
        for child in self.children:
            found_items.extend(child.query(search_bounds))
            
        return found_items

class GeometryScene(QGraphicsScene):
    """Scene that handles clicks to draw lines between points."""
    def __init__(self, parent=None):
        super().__init__(parent)
        self.parent_widget = parent
        self.last_clicked_point = None
        self.drawing_mode = True
        self.preview_line = None
        
    def mousePressEvent(self, event):
        """Handle mouse press events for point/line selection and line drawing."""
        scene_pos = event.scenePos()
        items = self.items(scene_pos)
        
        if not items:
            # Clicked empty space - clear selection
            self.clearSelection()
            self.last_clicked_point = None
            super().mousePressEvent(event)
            return
            
        top_item = items[0]
        
        if isinstance(top_item, GeometryPoint):
            self.handle_point_click(top_item, event)
        elif isinstance(top_item, GeometryLine):
            self.handle_line_click(top_item, event)
        else:
            super().mousePressEvent(event)
            
    def handle_point_click(self, point, event):
        """Handle clicks on points."""
        if not self.drawing_mode:
            super().mousePressEvent(event)
            return
            
        if self.last_clicked_point is None:
            # Start drawing a line
            self.last_clicked_point = point
            point.setBrush(QBrush(QColor("red")))  # Visual feedback
            
            # Create preview line
            pos = point.scenePos()
            self.preview_line = self.addLine(
                pos.x(), pos.y(), pos.x(), pos.y(),
                QPen(QColor("gray"), 2, Qt.DashLine)
            )
        else:
            if point != self.last_clicked_point:
                # Complete the line
                self.create_line(self.last_clicked_point, point)
                
            # Reset state
            self.last_clicked_point.setBrush(QBrush(QColor("dodgerblue")))
            if self.preview_line:
                self.removeItem(self.preview_line)
                self.preview_line = None
            self.last_clicked_point = None
            
    def handle_line_click(self, line, event):
        """Handle clicks on lines."""
        if self.drawing_mode:
            return  # Ignore line clicks in drawing mode
            
        if event.modifiers() == Qt.ControlModifier:
            # Multi-select with Ctrl
            line.setSelected(not line.isSelected())
        else:
            # Single select
            self.clearSelection()
            line.setSelected(True)
            
    def mouseMoveEvent(self, event):
        """Update preview line during drawing."""
        if self.preview_line and self.last_clicked_point:
            start_pos = self.last_clicked_point.scenePos()
            end_pos = event.scenePos()
            self.preview_line.setLine(
                start_pos.x(), start_pos.y(),
                end_pos.x(), end_pos.y()
            )
        super().mouseMoveEvent(event)
        
    def create_line(self, start_point, end_point):
        """Create a new line between points."""
        start_pos = start_point.scenePos()
        end_pos = end_point.scenePos()
        
        line = GeometryLine.from_points(
            start_pos, end_pos,
            GeometryLine.Type.CUSTOM
        )
        line.set_endpoints(start_point.number, end_point.number)
        self.addItem(line)
        
        if self.parent_widget:
            self.parent_widget.custom_lines.append(line)
            
        return line

class GeometryVisualizerView(QGraphicsView):
    """View that handles zooming and general interaction."""
    def __init__(self, scene, parent=None):
        super().__init__(scene, parent)
        self.parent_widget = parent
        
        # Setup view properties
        self.setRenderHint(QPainter.Antialiasing)
        self.setViewportUpdateMode(QGraphicsView.FullViewportUpdate)
        self.setHorizontalScrollBarPolicy(Qt.ScrollBarAlwaysOff)
        self.setVerticalScrollBarPolicy(Qt.ScrollBarAlwaysOff)
        self.setTransformationAnchor(QGraphicsView.AnchorUnderMouse)
        self.setResizeAnchor(QGraphicsView.AnchorUnderMouse)
        
        # Initialize state
        self.setDragMode(QGraphicsView.RubberBandDrag)
        self.zoom_factor = 1.15
        self.drawing_mode = True

    def set_drawing_mode(self, enabled):
        """Update drawing mode and cursor"""
        self.drawing_mode = enabled
        if enabled:
            self.setDragMode(QGraphicsView.NoDrag)
            self.setCursor(Qt.CrossCursor)
        else:
            self.setDragMode(QGraphicsView.ScrollHandDrag)
            self.setCursor(Qt.ArrowCursor)
            
    def handle_line_click(self, line, event):
        """Handle clicks on lines"""
        if self.drawing_mode:
            return  # Ignore line clicks in drawing mode
            
        if event.modifiers() == Qt.ControlModifier:
            # Multi-select with Ctrl
            line.setSelected(not line.isSelected())
        else:
            # Single select
            self.scene().clearSelection()
            line.setSelected(True)
            
    def wheelEvent(self, event):
        """Handle zooming with mouse wheel."""
        if event.modifiers() == Qt.ControlModifier:
            if event.angleDelta().y() > 0:
                self.scale(self.zoom_factor, self.zoom_factor)
            else:
                self.scale(1 / self.zoom_factor, 1 / self.zoom_factor)
            event.accept()
        else:
            super().wheelEvent(event)
            
    def keyPressEvent(self, event):
        """Handle keyboard shortcuts."""
        if event.key() == Qt.Key_Space:
            # Toggle drag mode
            if self.dragMode() == QGraphicsView.RubberBandDrag:
                self.setDragMode(QGraphicsView.ScrollHandDrag)
            else:
                self.setDragMode(QGraphicsView.RubberBandDrag)
        else:
            super().keyPressEvent(event)

